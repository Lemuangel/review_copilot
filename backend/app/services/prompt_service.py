from langchain_core.prompts import PromptTemplate

# ============================================================
# 差评分析 Prompt
# ============================================================

review_analysis_prompt = PromptTemplate(
    input_variables=["review_text"],
    template="""你是一位资深的跨境电商运营专家。请分析以下用户差评，识别出产品存在的问题。

## 分析维度
请从以下维度中识别问题（可多选）：
- 产品质量：材质差、做工粗糙、容易损坏、与描述不符
- 包装问题：包装简陋、破损、无保护措施
- 物流问题：配送慢、包裹丢失、物流信息不更新
- 尺寸问题：偏大/偏小、尺码不准、与标注不符
- 色差问题：实物与图片颜色差异大
- 功能问题：无法正常使用、功能缺失、操作复杂
- 售后问题：客服态度差、退换货困难、响应慢
- 价格问题：价格偏高、性价比低

## 输出格式
请以纯JSON格式返回结果，不要包含任何markdown代码块标记：
{{
    "issues": ["问题1", "问题2"],
    "sentiment": "negative",
    "severity": "high|medium|low"
}}

## 用户评论
{review_text}

请分析：""",
)


# ============================================================
# 运营建议 Prompt
# ============================================================

operation_suggestion_prompt = PromptTemplate(
    input_variables=["review_text", "issues"],
    template="""你是一位资深的跨境电商运营顾问。基于差评分析结果，请给出具体可落地的运营优化建议。

## 已知问题
{issues}

## 原始评论
{review_text}

## 要求
- 每条建议要具体、可执行
- 区分短期可解决的（1-2周）和长期改进的（1-3个月）
- 考虑成本和实施难度

## 输出格式
请以纯JSON格式返回结果，不要包含任何markdown代码块标记：
{{
    "suggestions": [
        "建议1",
        "建议2"
    ],
    "priority": ["高", "中"],
    "estimated_impact": "高|中|低"
}}

请给出建议：""",
)


# ============================================================
# 客服回复 Prompt
# ============================================================

customer_reply_prompt = PromptTemplate(
    input_variables=["review_text", "language"],
    template="""你是一位专业的跨境电商客服代表。请针对以下差评，撰写一条专业、真诚的客服回复。

## 回复要求
- 语气真诚、专业，不要过于模板化
- 先表达歉意和理解
- 针对具体问题给出解决方案或补偿
- 留下进一步沟通的渠道
- 长度控制在150字以内
- 使用{language}回复

## 差评内容
{review_text}

## 输出格式
请以纯JSON格式返回结果，不要包含任何markdown代码块标记：
{{
    "reply": "客服回复文本"
}}

请撰写回复：""",
)
